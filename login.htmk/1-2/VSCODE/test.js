// 1. 기본 제어 및 선언 (문법의 뼈대)
// let / const: 변수 선언 (변하는 값 / 고정된 값).
// if / else if / else: 조건에 따라 실행할 코드 나누기.
// switch: 여러 케이스 중 하나를 선택.
// for / while / do...while: 반복 작업 실행.
// break / continue: 반복문 탈출 / 다음 반복으로 건너뛰기.
// return: 함수의 결과값을 밖으로 내보내기.
// 2. 숫자 관련 (Math 객체)
// Math.abs(): 절대값.
// Math.min() / Math.max(): 최솟값 / 최댓값 찾기.
// Math.ceil() / Math.floor() / Math.round(): 올림 / 내림 / 반올림.
// Math.sqrt(): 제곱근(루트).
// Math.random(): 0~1 사이의 랜덤한 숫자 생성.
// 3. 배열(리스트) 조작 (가장 많이 씀)
// push() / pop(): 맨 뒤에 추가 / 맨 뒤 제거.
// unshift() / shift(): 맨 앞에 추가 / 맨 앞 제거.
// splice(): 원하는 위치의 요소 삭제 또는 추가.
// slice(): 배열의 일부분만 잘라내서 가져오기.
// concat(): 두 배열 합치기.
// indexOf() / includes(): 특정 값이 어디 있는지 찾기 / 있는지 확인.
// join(): 배열 요소를 하나의 문자열로 합치기.
// sort(): 정렬하기.
// 4. 문자열(글자) 조작
// split(): 문자열을 특정 기준(쉼표 등)으로 잘라 배열로 만들기.
// replace() / replaceAll(): 특정 글자를 다른 글자로 바꾸기.
// substring() / slice(): 글자 일부분만 추출.
// toLowerCase() / toUpperCase(): 소문자 / 대문자로 변환.
// trim(): 양 끝 공백 제거.
// 5. 고차 함수 (배열 마스터용)
// forEach(): 배열 요소를 하나씩 꺼내서 작업.
// map(): 배열의 모든 요소를 규칙에 따라 변형해서 새 배열 만들기.
// filter(): 조건에 맞는 요소만 골라내기.
// reduce(): 배열의 모든 값을 하나로 합치기 (합계 구할 때 유용).
// 6. 출력 및 디버깅
// console.log(): 콘솔창에 내용 출력 (가장 중요!).
// alert(): 브라우저에 경고창 띄우기.
// typeof: 이 변수가 숫자인지 문자인지 확인.
