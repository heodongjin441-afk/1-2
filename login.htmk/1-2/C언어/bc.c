#include <stdio.h>

int main(){
    // variable = A resuable container for a value.
    //            Behaves as if it were the value it contains.
    char grade = 'F';
    char symbol = '!';
    char currency = '$';
    
    printf("Your grade is %c\n", grade);
    printf("Your favorite symbol is %c\n", symbol);
    printf("Your currency is %c\n", currency);
    return 0;
}